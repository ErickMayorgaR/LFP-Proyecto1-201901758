

class RutaObj:
    def __init__(self, nombre, peso, inicio, fin):
        self.nombre = nombre
        self.peso = peso
        self.inicio = inicio
        self.fin = fin

    def get_nombre(self):
        return self.nombre

    def set_nombre(self, nombre):
        self.nombre = nombre


    def get_peso(self):
        return self.peso


    def set_peso(self, peso):
        self.peso = peso


    def get_inicio(self):
        return self.inicio

    def set_inicio(self, inicio):
        self.inicio = inicio

    def get_fin(self):
        return self.fin

    def set_fin(self, fin):
        self.fin = fin



